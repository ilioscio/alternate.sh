// Command gen generates the shared 64×64 blue-noise dither mask using the
// void-and-cluster method (Ulichney, "The void-and-cluster method for dither
// array generation", 1993) and writes it as both Go and JS sources:
//
//	internal/av/bluenoise.go
//	web/js/bluenoise.js
//
// Both implementations must threshold against the identical mask — the mask
// is part of the codec-parity contract, and a Go test asserts the two files
// agree. Generation is deterministic (fixed seed), and the outputs are
// committed; this only needs re-running if the mask parameters change.
//
// Run via: go generate ./internal/av
package main

import (
	"fmt"
	"math"
	"math/rand"
	"os"
	"path/filepath"
	"strings"
)

const (
	n    = 64 // mask side; must be a power of two (tiling uses & (n-1))
	size = n * n

	// sigma is the Gaussian filter width in pixels. Ulichney's classic
	// choice; larger values push energy to lower frequencies (less "blue").
	sigma = 1.5

	// seed fixes the initial random pattern. 4119 is the ASSP port.
	seed = 4119

	// initialOnes seeds the relaxation with a sparse random pattern (~10%).
	initialOnes = size / 10
)

// kernel[dy*n+dx] is the toroidal Gaussian weight at wrapped offset (dx, dy).
var kernel [size]float64

func initKernel() {
	for dy := 0; dy < n; dy++ {
		for dx := 0; dx < n; dx++ {
			wx := min(dx, n-dx)
			wy := min(dy, n-dy)
			kernel[dy*n+dx] = math.Exp(-float64(wx*wx+wy*wy) / (2 * sigma * sigma))
		}
	}
}

// field is a binary pattern plus, for every pixel, the Gaussian-filtered
// density of set pixels ("energy"). add/remove update energy incrementally.
type field struct {
	pattern [size]bool
	energy  [size]float64
}

func (f *field) add(i int)    { f.pattern[i] = true; f.splat(i, +1) }
func (f *field) remove(i int) { f.pattern[i] = false; f.splat(i, -1) }

func (f *field) splat(i int, sign float64) {
	iy, ix := i/n, i%n
	for dy := 0; dy < n; dy++ {
		row := ((iy + dy) & (n - 1)) * n
		krow := dy * n
		for dx := 0; dx < n; dx++ {
			f.energy[row+((ix+dx)&(n-1))] += sign * kernel[krow+dx]
		}
	}
}

// tightestCluster returns the set pixel with the highest energy.
// Ties break to the lowest index so generation is fully deterministic.
func (f *field) tightestCluster() int {
	best, bestE := -1, math.Inf(-1)
	for i := 0; i < size; i++ {
		if f.pattern[i] && f.energy[i] > bestE {
			best, bestE = i, f.energy[i]
		}
	}
	return best
}

// largestVoid returns the unset pixel with the lowest energy.
func (f *field) largestVoid() int {
	best, bestE := -1, math.Inf(1)
	for i := 0; i < size; i++ {
		if !f.pattern[i] && f.energy[i] < bestE {
			best, bestE = i, f.energy[i]
		}
	}
	return best
}

func (f *field) clone() *field {
	g := *f
	return &g
}

func generate() [size]uint8 {
	initKernel()
	rng := rand.New(rand.NewSource(seed))

	// Initial pattern: sparse random ones…
	var f field
	for _, p := range rng.Perm(size)[:initialOnes] {
		f.add(p)
	}
	// …relaxed until moving the tightest cluster into the largest void
	// would put it right back: the prototype binary pattern.
	for iter := 0; iter < 10*size; iter++ {
		c := f.tightestCluster()
		f.remove(c)
		v := f.largestVoid()
		f.add(v)
		if v == c {
			break
		}
	}

	var rank [size]int

	// Phase 1: rank the prototype's ones from initialOnes-1 down to 0 by
	// repeatedly removing the tightest cluster.
	{
		g := f.clone()
		for r := initialOnes - 1; r >= 0; r-- {
			c := g.tightestCluster()
			g.remove(c)
			rank[c] = r
		}
	}

	// Phase 2: from the prototype, fill the largest void until half full.
	g := f.clone()
	for r := initialOnes; r < size/2; r++ {
		v := g.largestVoid()
		g.add(v)
		rank[v] = r
	}

	// Phase 3: ones are now the majority. Track the energy of the *zeros*
	// and convert the tightest cluster of zeros first.
	{
		var z field
		for i := 0; i < size; i++ {
			if !g.pattern[i] {
				z.add(i)
			}
		}
		for r := size / 2; r < size; r++ {
			c := z.tightestCluster()
			z.remove(c)
			rank[c] = r
		}
	}

	// rank ∈ [0, 4096) → threshold ∈ [0, 256): exactly 16 pixels per level.
	var mask [size]uint8
	for i, r := range rank {
		mask[i] = uint8(r >> 4)
	}
	return mask
}

func formatRows(mask [size]uint8, indent string) string {
	var sb strings.Builder
	for y := 0; y < n; y++ {
		sb.WriteString(indent)
		for x := 0; x < n; x++ {
			fmt.Fprintf(&sb, "%d,", mask[y*n+x])
			if x != n-1 {
				sb.WriteByte(' ')
			}
		}
		sb.WriteByte('\n')
	}
	return sb.String()
}

func main() {
	mask := generate()

	goSrc := fmt.Sprintf(`// Code generated by internal/av/gen. DO NOT EDIT.
//
// 64×64 blue-noise dither mask (void-and-cluster, sigma %.1f, seed %d).
// Must stay identical to web/js/bluenoise.js; a test enforces this.

package av

const (
	blueNoiseSize  = %d
	blueNoiseShift = 6 // log2(blueNoiseSize)
)

var blueNoiseMask = [%d]uint8{
%s}
`, sigma, seed, n, size, formatRows(mask, "\t"))

	jsSrc := fmt.Sprintf(`// Code generated by internal/av/gen. DO NOT EDIT.
//
// 64×64 blue-noise dither mask (void-and-cluster, sigma %.1f, seed %d).
// Must stay identical to internal/av/bluenoise.go; a test enforces this.

export const BLUE_NOISE_SIZE = %d;

export const BLUE_NOISE_MASK = new Uint8Array([
%s]);
`, sigma, seed, n, formatRows(mask, "  "))

	root := findRoot()
	must(os.MkdirAll(filepath.Join(root, "web", "js"), 0o755))
	must(os.WriteFile(filepath.Join(root, "internal", "av", "bluenoise.go"), []byte(goSrc), 0o644))
	must(os.WriteFile(filepath.Join(root, "web", "js", "bluenoise.js"), []byte(jsSrc), 0o644))
	fmt.Println("wrote internal/av/bluenoise.go and web/js/bluenoise.js")
}

// findRoot walks up from the working directory to the module root (go.mod).
func findRoot() string {
	dir, err := os.Getwd()
	must(err)
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			panic("gen: go.mod not found above working directory")
		}
		dir = parent
	}
}

func must(err error) {
	if err != nil {
		panic(err)
	}
}
